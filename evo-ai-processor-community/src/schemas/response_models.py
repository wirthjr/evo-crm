# Integration Schemas
# ============================================================================

class IntegrationConfigResponse(BaseModel):
    """Integration configuration response data"""
    provider: str = Field(..., description="Integration provider name")
    connected: bool = Field(..., description="Whether the integration is connected")
    config: Optional[Dict[str, Any]] = Field(None, description="Additional configuration")


class IntegrationListResponse(BaseModel):
    """List of all integration configurations"""
    configs: Dict[str, IntegrationConfigResponse] = Field(..., description="Integration configurations by provider")
    credentials_configured: Dict[str, bool] = Field(..., description="Whether credentials are configured for each provider")


class OAuthMetadataResponse(BaseModel):
    """OAuth metadata response data"""
    authorization_servers: List[Dict[str, Any]] = Field(..., description="Available authorization servers")
    scopes_supported: List[str] = Field(default_factory=list, description="Supported OAuth scopes")
    response_types_supported: List[str] = Field(default_factory=list, description="Supported response types")
    grant_types_supported: List[str] = Field(default_factory=list, description="Supported grant types")


class OAuthAuthorizationResponse(BaseModel):
    """OAuth authorization URL response"""
    url: str = Field(..., description="OAuth authorization URL")


class OAuthCallbackResponse(BaseModel):
    """OAuth callback response data"""
    username: Optional[str] = Field(None, description="Username from OAuth provider")
    email: Optional[str] = Field(None, description="Email from OAuth provider")


