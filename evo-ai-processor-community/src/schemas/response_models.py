class UsageData(BaseModel):
    """Usage data for a client"""
    sessions_this_month: str = Field(..., description="Sessions used this month (format: 'used/limit')")
    active_sessions: str = Field(..., description="Active sessions (format: 'current/limit')")
    memory_retention_days: str = Field(..., description="Memory retention days")
    old_memory_entries: int = Field(..., description="Number of old memory entries")


class UsageSummaryResponse(BaseModel):
    """Usage summary response data"""
    usage: UsageData = Field(..., description="Usage statistics")
    warnings: List[str] = Field(default_factory=list, description="Usage warnings")


class UsageSummaryDictResponse(BaseModel):
    """Usage summary dictionary response"""
    pass  # This will be Dict[str, UsageSummaryResponse] in practice


