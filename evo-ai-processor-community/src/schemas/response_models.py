class CalendarItem(BaseModel):
    """Calendar item model"""
    id: str = Field(..., description="Calendar ID")
    name: str = Field(..., description="Calendar name")
    email: Optional[str] = Field(None, description="Calendar email")
    primary: Optional[bool] = Field(False, description="Whether this is the primary calendar")
    selected: Optional[bool] = Field(False, description="Whether this calendar is selected")


class GoogleCalendarCallbackResponse(BaseModel):
    """Google Calendar OAuth callback response"""
    email: Optional[str] = Field(None, description="User email")
    calendars: List[CalendarItem] = Field(default_factory=list, description="List of available calendars")


class CalendarsListResponse(BaseModel):
    """List of calendars response"""
    calendars: List[CalendarItem] = Field(..., description="List of calendars")


class AvailabilitySlot(BaseModel):
    """Availability slot model"""
    start: str = Field(..., description="Slot start time (ISO format)")
    end: str = Field(..., description="Slot end time (ISO format)")


class AvailabilityResponse(BaseModel):
    """Availability check response"""
    available: bool = Field(..., description="Whether the calendar is available")
    slots: List[AvailabilitySlot] = Field(default_factory=list, description="List of available time slots")


class CreateEventResponse(BaseModel):
    """Create event response"""
    success: bool = Field(..., description="Whether the event was created successfully")
    eventId: Optional[str] = Field(None, description="Created event ID")
    meetLink: Optional[str] = Field(None, description="Google Meet link if requested")


class DisconnectResponse(BaseModel):
    """Disconnect response"""
    success: bool = Field(..., description="Whether the disconnection was successful")


